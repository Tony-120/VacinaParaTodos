    private void jtargKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtargKeyReleased
        // TODO add your handling code here:
        
        String escolha = jctipo.getSelectedItem().toString().trim();
        String tipo = escolha.toLowerCase();        
        // System.out.println(tipo);
        String arg = jtarg.getText();

        DefaultTableModel mp1 = (DefaultTableModel) tbConsul.getModel();
        int l = mp1.getRowCount();

        if (l > 0) {
            while (l > 0) {
                ((DefaultTableModel) tbConsul.getModel()).removeRow(l - 1); // menos um pois a primeira  linha é zero
                l--;
            }//  fim do while
        }// fim do if-verifica se tem alguma informação tabela

        // fim do limpa a tabela
        try {
            ResultSet rs = ConsultaUsuDAO.getResultSet(tipo, arg);

            DefaultTableModel mp = (DefaultTableModel) tbConsul.getModel();

            while (rs.next()) {
                // pega os dados da linha do banco de dados
                String Coluna0 = rs.getString("iduser").toString().trim();
                String Coluna1 = rs.getString("nome").toString().trim();
                String Coluna2 = rs.getString("fone").toString().trim();
                String Coluna3 = rs.getString("login").toString().trim();
              
                
                // preenche a linha JTable e volta  para o while até terminar as linhas contidas
                // na tabela do banco.
                mp.addRow(new String[] {Coluna0,Coluna1,Coluna2,Coluna3});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro:"+e, "Preencher Item",2);
        }
        
        tamanhocolunas();
        tbConsul.setAutoCreateRowSorter(true); // ordena as linhas ao clicar na coluna

    }//GEN-LAST:event_jtargKeyReleased


