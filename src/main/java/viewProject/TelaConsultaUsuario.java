    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        DefaultTableModel mp1 = (DefaultTableModel) tbConsul.getModel();
        int l = mp1.getRowCount();

        if (l > 0) {
            while (l > 0) {
                ((DefaultTableModel) tbConsul.getModel()).removeRow(l - 1); // -1 pois a primeira linha é a linha ZERO
                l--;
            }
        } // Fim do while

        try {
            // Sem argumento na pesquisa:
            ResultSet rs = ConsultaUsuDAO.carregaTabela();

            // Instancia o objeto mp da classe DefaultTableModel
            DefaultTableModel mp = (DefaultTableModel) tbConsul.getModel();

            while (rs.next()) {
                // pega os dados da linha no banco de dados:
                String Coluna0 = rs.getString("iduser").toString().trim();
                String Coluna1 = rs.getString("nome").toString().trim();
                String Coluna2 = rs.getString("fone").toString().trim();
                String Coluna3 = rs.getString("login").toString().trim();

                mp.addRow(new String[]{Coluna0, Coluna1, Coluna2, Coluna3});

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro:" + e, "Preencher Item", 2);
        }
        tamanhocolunas();
        tbConsul.setAutoCreateRowSorter(true);// ordena as linhas ao clicar na coluna.
    }//GEN-LAST:event_formInternalFrameOpened

